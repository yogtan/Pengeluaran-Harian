    /*Di button cari ini akan menjalan pencarian dengan menggunakan metode searchong objek
    dengan cara sequential search, seperti berikut. Kami membuat metode sequential search dengan parameter .......
    di metode ini akan dilakukan percabangan yang dimana ketika indek kurang dari jumlah
    maka masuk ke percabangan lafi dengan parameterr .... maka akan masuk ke percabangan ketika
    user memilih rb nama penyewa, maka akan dicek apakah....*/
    Object sequentialSearch(Object x, int index, Object obj, int jumlah) {
        int jum = -1;
        if (index < jumlah) {
            if (obj == datPen.sewa) {
                if (rbPendapatan.isSelected()) {
                    //Parameter obj sama dengan isi dari array list sewa yang disimpan dalam kelas datakost yang diakses menggunakan nama objek datPen
                    if (((String) x).equalsIgnoreCase(datPen.sewa.get(index).nama)) {
                        jum = 0;
                        //Ketika percabangan ini bernilai benar maka indeknya akan disimpan dan datanya akan ditampilkan
                        TampilDataCari(index);
                    } else {
                        //Jika tidak maka indeknya akan bertambah satu sesuai dengan model sequential dan metode ini akan dipanggil kembali

                        index++;
                        return sequentialSearch(x, index, obj, jumlah);
                    }
                } else { //No Kamar                    
                    if (Integer.parseInt(((String) x)) == datPen.sewa.get(index).nokost) {
                        jum = 0;
                        TampilDataCari(index);
                    } else {
                        index++;
                        return sequentialSearch(x, index, obj, jumlah);
                    }
                }
            } else if (obj == datPen.kamarList) {
                if (((int) x) == datPen.kamarList.get(index).no_kost) {
                    return index;
                } else {
                    index++;
                    return sequentialSearch(x, index, obj, jumlah);
                }
            }
        } else if (index == jumlah - 1 && jum != 0) {
            JOptionPane.showMessageDialog(this, "Data tidak ditemukan", "Error", JOptionPane.ERROR_MESSAGE);
        }
        return jum;
    }

