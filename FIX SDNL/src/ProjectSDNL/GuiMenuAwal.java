    public void TampilData(String jenis, String jenisKel) {
        String[] namaKolom = {"No_kost", "Nama", "Kategori", "Lama Sewa"};
        tabelModel tblModel;
        if (jenis.equals("Hapus")) {
            tblModel = new tabelModel(namaKolom, datPen, jenisKel);
            jTabelHapus.setModel(tblModel);
            jTabelHapus.revalidate();
        } else if (jenis.equals("Tampil")) {
            if (jenisKel == null) {
                tblModel = new tabelModel(namaKolom, datPen, jenisKel);
                jTable2.setModel(tblModel);
                jTable2.revalidate();
            } else if (jenisKel.equals("KamarKosong")) {
                String[] kolKosong = {"No Kamar", "Kategori", "Alamat", "Harga Sewa"};
                tblModel = new tabelModel(kolKosong, datPen, jenisKel);
                jTabelKosong.setModel(tblModel);
                jTable2.revalidate();
            }

        }

    }

    public Boolean cek(Object cek, JTextField txt) {
        try {
            if (txt == txtTanggal) {
                if (!((String) cek).matches(".*\\d.*")) {
                    txtTanggal.setText((String) cek);
                } else {
                    throw new Exception("Nama tidak boleh berisi nomor");
                }
            } else if (txt == txtPendapatan) {
                if (((String) cek).matches("[0-9]+")) {
                    txtPendapatan.setText(((String) cek));
                } else {
                    throw new Exception("NIK Hanya Boleh Berisi Angka");
                }
//            } else if (txt == txtAlamat) {
//                if (((String) cek).matches("[0-9]+")) {
//                    txtAlamat.setText(((String) cek));
//                } else {
//                    throw new Exception("NIK Hanya Boleh Berisi Angka");
//                }
            } else if (txt == txtCatatan) {
                if (((String) cek).matches("[0-9]+")) {
                    txtCatatan.setText(((String) cek));
                } else {
                    throw new Exception("NIK Hanya Boleh Berisi Angka");
                }
            } else if (txt == txtCari) {
                if (rbPengeluaran.isSelected()) {
                    if (((String) cek).matches("[0-9]+")) {
                        txtCari.setText((String) cek);
                        return true;
                    } else {
                        throw new Exception("Nomor kamar harus berupa angka");
                    }
                } else if (rbPendapatan.isSelected()) {
                    if (!((String) cek).matches(".*\\d.*")) {
                        txtCari.setText((String) cek);
                        return true;
                    } else {
                        throw new Exception("Nama tidak boleh mengandung angka");
                    }
                }

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "PERINGATAN", JOptionPane.ERROR_MESSAGE);
        }
        return false;
    }

